package DataTypeConversions;

public class ExplicitConversions 
{
	public static void main(String[] args) 
	{
		float f = 12.9F;
		int a = (int)f;
		
		float g = 13.4f;
		int b = (int)g;
		
		System.out.println("Value of a: "+a); //explicit conversions
		System.out.println("Value of b: "+b); //explicit conversions
	}

}
